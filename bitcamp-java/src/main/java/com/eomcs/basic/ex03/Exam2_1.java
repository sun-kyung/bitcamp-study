package com.eomcs.basic.ex03;

//정수 리터럴(literal)

public class Exam2_1 {
  public static void main(String[] args) {
    // 10진수 리터럴
    System.out.println(100);

    // 8진수 리터럴
    System.out.println(0144);

    // 2진수
    System.out.println(0b01100100);
    System.out.println(0B01100100);

    // 16진수
    System.out.println(0x64);
    System.out.println(0X64);


  }
}