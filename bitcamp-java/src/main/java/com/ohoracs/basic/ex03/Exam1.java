package com.ohoracs.basic.ex03;

//리터럴(literal)
//-자바언어에서 값을 표기하는 문법
public class Exam1 {
  public static void main(String[] args) {
    //정수 리터럴
    System.out.println(78); //10진수표기법
    //78이 정수 리터럴이다.

    System.out.println(+78); //10진수표기법
    System.out.println(-78); //10진수표기법

    System.out.println(0116); //8진수표기법
    System.out.println(-0116); //8진수표기법
  
    System.out.println(0x4e); //16진수표기법
    System.out.println(-0x4e); //16진수표기법

    System.out.println(0b01001110); //2진수표기법

    //부동소수점 리터럴

    //논리 리터럴

    //문자 리터럴
  


  }
}